void spiwrite(uint8_t address, uint8_t value)
{
	uint8_t data[2];
	data[0] = address|0x40;  //Or with 0x40 to do write/read multiple bits 
	data[1] = value;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);  // make chip select low to enable transmission
	HAL_SPI_Transmit(&hspi1, data, 2, 10);  // write data to register
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);  // make chip select high at the end of transmission
}
void spiread(uint8_t address)
{
	address |= 0x80;  // //Or with 0x80 for read operations
	address |= 0x40;  
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET); 
	HAL_SPI_Transmit(&hspi1, &address, 1, 1);  // send address
	HAL_SPI_Receive(&hspi1, &spibuf[0], 6, 1);  // read the data from device
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET); 
}
